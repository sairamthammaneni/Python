s1={1,2,'a','i','d',3,4,5}
s2={1,4,3,'a','b','c','d','e'}
print(s1)
print(s2)
print(s1|s2)	#union
print(s1&s2)	#intersection
print(s1-s2)	#set differenece
print(s1^s2)	#symmetric differnce
print('i' in s1)	#set membership
